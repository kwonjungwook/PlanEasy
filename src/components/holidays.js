  // 모든 휴일을 하나의 객체로 통합
  export const HOLIDAYS = {
    ...HOLIDAYS_2024_2030,
    ...HOLIDAYS_2031_2040
  };
  
  // 휴일 관련 유틸리티 함수들
  export const isHoliday = (dateString) => {
    return HOLIDAYS[dateString] !== undefined;
  };
  
  export const getHolidayName = (dateString) => {
    return HOLIDAYS[dateString] || null;
  };
  
  // 월별로 휴일 목록을 가져오는 함수
  export const getHolidaysForMonth = (year, month) => {
    // month는 1~12 사이의 값
    const monthStr = month.toString().padStart(2, '0');
    const prefix = `${year}-${monthStr}`;
    
    return Object.entries(HOLIDAYS)
      .filter(([date]) => date.startsWith(prefix))
      .reduce((acc, [date, name]) => {
        acc[date] = name;
        return acc;
      }, {});
  };
  
  // 연도별로 휴일 목록을 가져오는 함수
  export const getHolidaysForYear = (year) => {
    const prefix = `${year}-`;
    
    return Object.entries(HOLIDAYS)
      .filter(([date]) => date.startsWith(prefix))
      .reduce((acc, [date, name]) => {
        acc[date] = name;
        return acc;
      }, {});
  };
  
  // 날짜가 주말인지 확인하는 함수
  export const isWeekend = (dateString) => {
    const date = new Date(dateString);
    const day = date.getDay(); // 0: 일요일, 6: 토요일
    return day === 0 || day === 6;
  };
  
  // 날짜가 휴일 또는 주말인지 확인하는 함수
  export const isHolidayOrWeekend = (dateString) => {
    return isHoliday(dateString) || isWeekend(dateString);
  };